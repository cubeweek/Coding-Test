package problem.baekjoon.gold.g4

import kotlin.math.min

fun main(args: Array<String>) {
    class FastReader {
        val buffer = ByteArray(1 shl 16)
        var lim = 0
        var cur = 0

        @Suppress("NOTHING_TO_INLINE")
        private inline fun readByte(): Int {
            if (cur >= lim) {
                lim = System.`in`.read(buffer)
                if (lim <= 0) return -1
                cur = 0
            }
            return buffer[cur++].toInt()
        }

        fun nextInt(): Int {
            var c = readByte()
            while (c in 0..32) c = readByte()
            if (c == -1) return 0

            var isNeg = false
            if (c == 45) {
                isNeg = true
                c = readByte()
            }

            var res = 0
            while (c in 48..57) {
                res = (res shl 3) + (res shl 1) + (c and 15)
                c = readByte()
            }
            return if(isNeg) -res else res
        }
    }

    val fs = FastReader()
    val cityCnt = fs.nextInt()
    val busCnt = fs.nextInt()
    val shortMap = Array(cityCnt + 1) { IntArray(cityCnt + 1) { 10_000_001 } }
    for (i in 1..cityCnt) { shortMap[i][i] = 0 }

    repeat (busCnt) {
        val src = fs.nextInt()
        val dst = fs.nextInt()
        val cost = fs.nextInt()
        shortMap[src][dst] = min(shortMap[src][dst], cost)
    }

    for (k in 1..cityCnt) {
        for (s in 1..cityCnt) {
            for (d in 1..cityCnt) {
                shortMap[s][d] = min(shortMap[s][d], shortMap[s][k] + shortMap[k][d])
            }
        }
    }

    val sb = StringBuilder()
    for (i in 1..cityCnt) {
        for (j in 1 until cityCnt) {
            sb.append("${if (10_000_001 == shortMap[i][j]) 0 else shortMap[i][j]} ")
        }
        sb.append("${if (10_000_001 == shortMap[i][cityCnt]) 0 else shortMap[i][cityCnt]}\n")
    }
    println(sb.toString())
}
